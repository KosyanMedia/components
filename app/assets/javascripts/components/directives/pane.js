angular.module('Components').
  directive('pane', [function() {
    return {
      require: '^tabs',
      restrict: 'E',
      transclude: true,
      scope: { title: '@' },
      link: function(scope, element, attrs, tabsCtrl) {
        var is_active = angular.isDefined(attrs.active);
        tabsCtrl.addPane(scope, is_active);
      },
      template:
        '<div class="tab-pane" ng-class="{active: selected}" ng-transclude>' +
        '</div>',
      replace: true
    };
  }]);
